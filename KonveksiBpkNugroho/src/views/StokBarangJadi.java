        public void autoKodeBrgJadiMasuk(){
        try{
            String sql = "SELECT MAX(right(kd_brg_jadi, 1)) AS kd_brg_jadi FROM tb_brg_jadi";
            Connection conn = (Connection)Koneksi.getKoneksi();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while(rs.next()){
                if(rs.first()==false){
                    txtKodeBarang.setText("KDBJ-001");
                }else{
                    rs.last();
                    int Kode = rs.getInt(1) + 1;
                    String nomor = String.valueOf(Kode);
                    int noSup = nomor.length();
                    
                    for (int i=0; i<3-noSup; i++){
                        nomor = "0" + nomor;
                    }
                    txtKodeBarang.setText("KDBJ-" + nomor);
                }
                txtKodeBarang.setEnabled(false);
            }
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Koneksi Database Gagal"+ e.getMessage());
        }
    }
